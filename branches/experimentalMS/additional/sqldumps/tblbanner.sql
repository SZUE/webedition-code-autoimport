CREATE TABLE tblbanner (
  ID bigint  NOT NULL IDENTITY(1,1) PRIMARY KEY,
  ParentID bigint  NOT NULL default '0',
  "Text" varchar(255) NOT NULL default '',
  Path varchar(255) NOT NULL default '',
  Icon varchar(64) NOT NULL default '',
  IsFolder tinyint  NOT NULL default '0',
  CreatorID int  NOT NULL default '0',
  CreateDate int  NOT NULL default '0',
  ModifierID int  NOT NULL default '0',
  ModifyDate int  NOT NULL default '0',
  bannerID bigint  NOT NULL default '0',
  bannerUrl varchar(255) NOT NULL default '',
  bannerIntID bigint  NOT NULL default '0',
  IntHref tinyint  NOT NULL default '0',
  maxShow int  NOT NULL default '0',
  maxClicks int  NOT NULL default '0',
  IsDefault tinyint  NOT NULL default '0',
  clickPrice float(53) NOT NULL default '0',
  showPrice float(53) NOT NULL default '0',
  StartOk tinyint  NOT NULL default '0',
  EndOk tinyint  NOT NULL default '0',
  StartDate int  NOT NULL default '0',
  EndDate int  NOT NULL default '0',
  FileIDs varchar(255) NOT NULL default '',
  FolderIDs varchar(255) NOT NULL default '',
  CategoryIDs text NOT NULL,
  DoctypeIDs varchar(255) NOT NULL default '',
  IsActive tinyint  NOT NULL default '0',
  clicks int  NOT NULL default '0',
  views int  NOT NULL default '0',
  Customers varchar(255) NOT NULL default '',
  TagName varchar(255) NOT NULL default '',
  weight tinyint  NOT NULL default '0'
) 
