CREATE TABLE tblObject (
  ID int  NOT NULL IDENTITY(1,1),
  ParentID int  NOT NULL default '0',
  strOrder text NOT NULL,
  "Text" varchar(255) NOT NULL default '',
  Icon varchar(64) NOT NULL default '',
  IsFolder tinyint  NOT NULL default '0',
  ContentType varchar(25) CHECK(ContentType IN ('object')) NOT NULL default 'object',
  CreationDate int  NOT NULL default '0',
  ModDate int  NOT NULL default '0',
  "Path" varchar(255) NOT NULL default '',
  CreatorID bigint  NOT NULL default '0',
  ModifierID bigint  NOT NULL default '0',
  RestrictOwners tinyint  NOT NULL default '0',
  Owners varchar(255) NOT NULL default '',
  OwnersReadOnly text NOT NULL,
  RestrictUsers tinyint  NOT NULL default '0',
  Users varchar(255) NOT NULL default '',
  UsersReadOnly text NOT NULL,
  DefaultCategory text NOT NULL,
  DefaultParentID bigint  NOT NULL default '0',
  DefaultText varchar(255) NOT NULL default '',
  DefaultValues text NOT NULL,
  DefaultDesc varchar(255) NOT NULL default '',
  DefaultTitle varchar(255) NOT NULL default '',
  DefaultKeywords varchar(255) NOT NULL default '',
  DefaultUrl varchar(255) NOT NULL default '',
  DefaultUrlfield0 varchar(255) NOT NULL DEFAULT '_',
  DefaultUrlfield1 varchar(255) NOT NULL DEFAULT '_',
  DefaultUrlfield2 varchar(255) NOT NULL DEFAULT '_',
  DefaultUrlfield3 varchar(255) NOT NULL DEFAULT '_',
  DefaultTriggerID bigint  NOT NULL default '0',
  ClassName varchar(64) NOT NULL default '',
  Workspaces varchar(1000) NOT NULL default '',
  DefaultWorkspaces varchar(1000) NOT NULL default '',
  Templates varchar(255) NOT NULL default '',
  CacheType  varchar(25) CHECK(CacheType IN ('','none','tag','document','full')) NOT NULL default 'none',
  CacheLifeTime int  NOT NULL default '0',
  PRIMARY KEY  (ID)
) 

CREATE INDEX idx_tblObject_Path ON tblObject(Path);
CREATE INDEX idx_tblObject_IsFolder ON tblObject(IsFolder);
